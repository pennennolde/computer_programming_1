package sec04;

class EscapeExample {

	public static void main(String[] args) {
		System.out.println( "ab\tcd" );
		System.out.println( "ab\ncd" );
		System.out.println( "ab\\cb" );
		System.out.println( "\"abcd\"" );
		System.out.println( "'" );
		System.out.println( 'A' );
		System.out.println( '\'' );
		System.out.println( "\u5678" );



	}

}
