package sec04;

class JaType {

	public static void main(String[] args) {
		boolean	bldt	=	true;
		char			cdt	=	'A';									//"A"とは違う
		byte			bdt	=	127;
		short		sdt	=	32767;
		int			idt	=	2147483647;
		long			ldt	=	9223372036854775807L;
		float			fdt	=	123.456F;
		double		ddt	=	123.456789012345;

		System.out.println(bldt);
		System.out.println(cdt);
		System.out.println(bdt);
		System.out.println(sdt);
		System.out.println(idt);
		System.out.println(ldt);
		System.out.println(fdt);
		System.out.println(ddt);


	}

}
